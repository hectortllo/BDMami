    private void Numeros(KeyEvent e, int limite, JTextField txtField) {
        if(txtField.getText().length() == limite){
            e.consume();
            if ((e.getKeyChar() < '0' || e.getKeyChar() > '9')) {                           //Si no es un numero se va a consumir 
                e.consume();
            }
        }
    }

