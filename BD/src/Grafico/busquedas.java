    private void Letras(KeyEvent e) {
        char c = e.getKeyChar();
        if ((c < 'a' || c > 'z') && (c < 'A' || c > 'Z')) {
            e.consume();
        }
    }

    private void puntoFlotante(KeyEvent e, JTextField txt) {
        if (!Character.isDigit(e.getKeyChar()) && e.getKeyChar() != '.' && e.getKeyChar() != 8) {
            e.consume();
        }
        if (e.getKeyChar() == '.' && txt.getText().contains(".")) {
            e.consume();
        }
    }

